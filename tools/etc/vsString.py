jp_tables = [jp_table_a, jp_table_b, jp_table_c, jp_table_d, jp_table_e, jp_table_g, jp_table_h, jp_table_i, jp_table_j, jp_table_k, jp_table_l]

# This isn't the only encoding used in the game, occasionally
# ASCII can be found, where a '#' character forces the string to
# anchor its end to the draw position rather than the start.

# Inline functions are denoted by enclosing the op and operand(s) with ||, 
# as it's the only common symbol not represented in the default text table.

# String functions, 1-byte operand
# 0xEC -> |mn|: Probably related to JP font table as well
# 0xED - 
# 0xF7 -> |a-ln|: Font table for Japanese, partially implemented, won't be needed when JP font is complete
# 0xF8 -> |!n|: Sets the text speed to n, where 0 = instantaneous
# 0xF9 -> |♪n|: Play sfx n after each chunk is rendered
# 0xFA -> |>n|: Advances the x offset by n pixels
# 0xFB -> |fn|: Manipulate font. n = 0-3 sets the color, n = 4 toggle text justification,
#               n = 5 or 6 set font table 1 and 0 respectively.
#               7 might be unintended, unsure what bit 3 is for but 7 will
#               enable it, as well as set the color to 3.
#               Values over 7 are shifted down by 3 and advance the y offset
# 0xFD -> |xn|: Inserts hex integer argument n
# 0xFE -> |#n|: Inserts decimal integer argument n
# 0xFF -> |$n|: Inserts string argument n

def decode(s, jpTable=False):
    baseTable = jp_table_0 if jpTable else table
    result = ""
    i = 0
    while i < len(s):
        match s[i]:
            case 0xE7:
                return result
            case 0xEB:  # 2-byte alignment, ignored
                i += 1
            case 0xED | 0xEE | 0xEF | 0xF0 | 0xF1 | 0xF2 | 0xF3 | 0xF4 | 0xF5 | 0xF6 | 0xF7:
                table_index = s[i] - 0xED
                jp_char = jp_tables[table_index][s[i + 1]]
                result += jp_char
                i += 2
            case 0xEC | 0xF8 | 0xF9 | 0xFA | 0xFB | 0xFD | 0xFE | 0xFF:
                result += f"{table[s[i]]}{s[i + 1]}|"
                i += 2
            case _:
                if baseTable[s[i]] == '':
                    raise ValueError(f"Invalid byte {s[i]} in vsString")
                result += baseTable[s[i]]
                i += 1
    return result

def encode_raw(s, jpTable=False):
    baseTable = jp_table_0 if jpTable else table
    result = []
    i = 0
    while i < len(s):
        if s[i] == '|':
            end = s.find('|', i + 3)
            if end == -1:
                raise ValueError("Unterminated control")
            if s[i+1] == '0':
                result.append(int(s[i+1:end], 16))
            else:
                result.extend([table.index(s[i:i+2]), int(s[i+2:end].strip())])
            i = end + 1
        elif s[i:i+3] == "Lv.":
            result.append(table.index("Lv."))
            i += 3
        else:
            try:
                idx = baseTable.index(s[i])
                result.append(idx)
            except ValueError:
                for table_idx, jp_table in enumerate(jp_tables):
                    try:
                        idx = jp_table.index(s[i])
                        result.extend([0xED + table_idx, idx])
                        break
                    except ValueError:
                        continue
                else:
                    raise ValueError(f"Character '{s[i]}' not found in any table")
            i += 1
    return bytes(result)

def encode(s, padding=0xEB, jpTable=False):
    result = list(encode_raw(s, jpTable))
    result.append(0xE7)
    if padding != None and len(result) % 2 == 1:
        result.append(padding)
    return bytes(result)


